export type BannerPositionType = 'main' | 'side' | 'category' | 'dual' | 'triple' | 'popup' | 'showHide';

/**
 * BannerPositionType 설명
 *
 * - main       : 홈 페이지 최상단 롤링 배너
 * - side       : 메인 배너 옆의 직사각형 배너
 * - category   : 카테고리 페이지 최상단 배너
 * - dual       : 홈 페이지 중간에 배치되는 최대 2개의 배너
 * - triple     : 홈 페이지 하단에 배치되는 최대 3개의 배너
 * - popup      : 창이 뜨고 닫기 버튼이 있는 배너
 * - showHide   : 버튼을 클릭하면 위로 펴지는 배너
 */
